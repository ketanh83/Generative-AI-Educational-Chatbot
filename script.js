function question() {
    fetch('http://127.0.0.1:5000/question?topic=' + window.mode)
        .then(response => {
            if (!response.ok) {
                throw new Error('Network response was not ok');
            }
            return response.json();
        })
        .then(data => {
            console.log(data);
            window.question = data["question"]
            postQuestion(data)
        })
}

